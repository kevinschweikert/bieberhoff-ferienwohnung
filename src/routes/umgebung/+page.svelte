<h1>Umgebung und Freizeitmöglichkeiten</h1>

<p>
	Unser Bieberhoff liegt ländlich mit nur wenigen Nachbarhöfen und z.T. freier
	Sicht auf die Felder und Wiesen. Die Ruhe und die weite, typisch ostfriesische
	Landschaft, das angrenzende Königsmoor laden genauso zu Fahrradtouren ein, wie
	die umliegenden Wälder zu ausgedehnten Spaziergängen.
</p>

<p>
	In ca. 15 Autominuten sind Sie im Zentrum von Aurich, wo Sie einen
	Kletterwald, sowie eine Kletterhalle, die größte Indoor-Skatehalle
	Deutschlands und viele weitere Freizeitmöglichkeiten und natürlich alle
	Einkaufsoptionen finden. Der nächste Supermarkt/Bäcker/ Apotheke ist ca. 8
	Auto- Minuten entfernt.
</p>

<p>
	Da Aurich mitten auf dem „Ostfriesenäquator“ liegt, ist die Stadt der perfekte
	Ausgangspunkt für alle möglichen Ziele in der Umgebung.
</p>

<p>
	Viele Ausflugshighlights wie das große Meer, das ewige Meer, Badeseen in
	Timmel, Wiesmoor oder Aurich (mit Wasserskianlage) sind genauso wie die
	Nordseeküste mit den Badeorten Bensersiel, Neuharlingersiel etc. in ca. 30
	Autominuten zu erreichen. Von dort aus können Sie dann auch gut mit der Fähre
	auf die ostfriesischen Inseln übersetzen (z.B. nach Langeoog und Spiekeroog).
</p>

<p>
	Die Hafenstadt Emden (z.B. mit der „Kunsthalle“ oder dem „Otto Huus“) kann man
	z.B. mit einer Bootstour ansteuern und auch der Weg nach Leer (mit dem
	„Leeraner Miniaturland“) durch das schöne ostfriesische Fehngebiet mit vielen
	Mühlen ist nicht weit. Ebenso laden hier die ostfriesischen Radwanderwege oder
	die Fehnroute zu ausgedehnten Touren ein.
</p>

<p>
	Nach Wilhelmshaven (z.B. mit dem deutschen Marinemuseum und dem „Aquarium“)
	oder nach Oldenburg sind Sie ca. 1 Stunde unterwegs.
</p>

<p>
	Für Tierfreunde gibt es z.B. die Seehundstation in Norden-Norddeich (ca. 45
	Autominuten) oder den Haustierpark in Werdum, sowie Birgits Tiergarten und
	Streichelzoo in Rechtsupweg. Und mit zusätzlicher „Action“ können der Tier-
	und Freizeitpark Thüle oder der Jaderpark (ca. 45 bis 60 Minuten) dienen.
</p>

<p>
	In den vielen kleinen ostfriesischen Orten können Sie aber auch immer wieder
	unerwartete charmante Überraschungen erleben (kleine, urige Teestuben,
	originelle Bazare oder Dorffeste, „bosselnde“ Ostfriesen u.v.m.)
</p>

<p>
	So finden Sie bei jedem Wetter und für jeden Anspruch bestimmt das richtige
	Ziel.
</p>
